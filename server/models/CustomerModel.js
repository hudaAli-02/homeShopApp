import mongoose from "mongoose";

const CustomerSchema = mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  country: {
    type: String,
    required: true,
  },
  profilePic: {
    type: String,
    default: "user.png",

  },
});

const CustomerModel = mongoose.model("customerInfos", CustomerSchema);
export default CustomerModel;

